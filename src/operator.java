public class operator
{
    public static void main(String[] args) {
        // comparison operator
        int a = 3, b = 4;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
